package basics;

public class Lecture8ForLoop {

	public static void main(String[] args) {
		
		//When we have to iterate over a series of numbers we use iterative statements
		//Like take an example for printing 10 number
		for(int i=1;i<=10;i++)
			System.out.println(i);
		//The for loop starts with a intial condition and the condition is checked
		//After checking the first condition the increment or decrement takes place
		//And then after completion of one iteration then increment and decrement takes place
		//And then again the condition is checked

	}

}
