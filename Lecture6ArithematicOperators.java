package basics;

public class Lecture6ArithematicOperators {

	public static void main(String[] args) {
		/*
		 + for addition
		 - for subtraction
		 * for multiplication
		 / for division
		  % for remainder
		 */
		int a=5;
		int b=3;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		// / this will gives us the quotient 
		// % will give you the remainder
	}

}
