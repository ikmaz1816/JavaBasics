package basics;

public class Lecture9WhileLoop {

	public static void main(String[] args) {
		//The while loop also follow the same fashion as for loop
		int i=1;//initialization
		while(i<=10)
		{
			System.out.println(i);
			i++;
		}
		// for loop and while loop both are entry controlled loop
		//that is the condition is checked intially
	}

}
