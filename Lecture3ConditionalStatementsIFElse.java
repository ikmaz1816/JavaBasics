package basics;

public class Lecture3ConditionalStatementsIFElse {

	public static void main(String[] args) {
		int a=6;
		if(a>5)
		{
			System.out.println("a is greater than 5");
		}
		else
		{
			System.out.println("a is lesser than 5");
		}
		//if the If condition fails then it makes a call to else
		//if the if condition is true then only one condition is checked and if If the condition is false
		//both the conditions are checked
		//Only one block is executed
		//Conditions check points can be multiple

	}

}
